    public void exibirPainel(String painel)
    {                
        CardLayout card = (CardLayout)backgroundPanel.getLayout();
        card.show(backgroundPanel, painel);
        alterarTitulo(painel);
    }    
    
    public void alterarTitulo(String painel)
    {
        switch (painel) {
            case "listarCliente":
                labelTitulo.setText("LISTA DE CLIENTES");
                listarCliente.AtualizarTabela();
                break;
            case "listarProduto":
                labelTitulo.setText("LISTA DE PRODUTOS");
                listarProduto.AtualizarTabela();
                break;                
            case "incluirCliente":
                labelTitulo.setText("CADASTRO DE CLIENTES");
                incluirCliente.carregarFormulario();
                break;                
            case "incluirProduto":
                labelTitulo.setText("CADASTRO DE PRODUTOS");
                incluirProduto.carregarFormulario();
                break;
            case "iniciarVenda":
                labelTitulo.setText("NOVO PEDIDO");
                break;    
            case "exibirRelatorio":
                labelTitulo.setText("RELATÓRIO DE VENDAS");
                break;        
            default:
                labelTitulo.setText("GAME STORE");
                break;
        }
    }
    
